// Define custom resolvers
const customResolvers = {
	Query: {
		personHolidayCount: async (_, { name }, context) => {
			// Implement the custom logic using the Neo4j driver
			const session = context.driver.session();
			try {
				const result = await session.run(
					`MATCH (p:Person {name: $name})-[:ATTENDED]->(h:Holiday)
           RETURN p.name AS personName, COUNT(h) AS holidayCount`,
					{ name },
				);
				const record = result.records[0];
				return {
					personName: record.get("personName"),
					holidayCount: record.get("holidayCount").toInt(),
				};
			} catch (error) {
				throw new Error("Error querying the Neo4j database", { cause: error });
			} finally {
				await session.close();
			}
		},
	},
};

